
public class TestandoWhile {
	public static void main(String[] args) {

		int contador = 0;

		while (contador <= 10) {

			System.out.println(contador);
			
			//contador += 1; 
			// contador = contador + 1;
			contador++;
		
		} // escopo do while
		System.out.println(contador);
	} // escopo do main

}
